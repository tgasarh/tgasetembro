def _get_gspread_client():
    # Usa secrets (Streamlit Cloud) se existir; sen√£o, arquivo local
    if "gcp_service_account" in st.secrets:
        creds = Credentials.from_service_account_info(
            st.secrets["gcp_service_account"], scopes=SCOPES
        )
    else:
        creds = Credentials.from_service_account_file(
            "service_account.json", scopes=SCOPES
        )
    return gspread.authorize(creds)

def _get_worksheet(client):
    """
    Abre (ou cria) a planilha 'Feedbacks_TGA' e a aba 'Respostas'.
    """
    try:
        sh = client.open("Feedbacks_TGA")
    except gspread.SpreadsheetNotFound:
        sh = client.create("Feedbacks_TGA")
    try:
        ws = sh.worksheet("Respostas")
    except gspread.WorksheetNotFound:
        ws = sh.add_worksheet(title="Respostas", rows=1000, cols=10)
        ws.append_row(["timestamp", "sentimento", "feedback"], value_input_option="USER_ENTERED")
    return ws

def append_feedback_to_sheet(sentimento: str, feedback: str):
    """
    Adiciona uma linha com data/hora, sentimento e feedback.
    """
    try:
        client = _get_gspread_client()
        ws = _get_worksheet(client)
        ts = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        ws.append_row([ts, sentimento, feedback], value_input_option="USER_ENTERED")
    except Exception as e:
        st.toast(f"N√£o foi poss√≠vel salvar no Google Sheets: {e}", icon="‚ö†Ô∏è")

